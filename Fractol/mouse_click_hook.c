#include "fractol.h"

int		mouse_click_hook(int k, int x, int y, t_env *e)
{
	if (k == 5)
	{
		e->z = e->z * 1.1;
		e->x = (e->x + x / 11) * 1.1;
		e->y = (e->y + y / 11) * 1.1;
		ft_refresh(e);
	}
	if (k == 4)
	{
		e->z = e->z / 1.1;
		e->x = (e->x - x / 11) / 1.1;
		e->y = (e->y - y / 11) / 1.1;
		ft_refresh(e);
	}
	return (0);
}
