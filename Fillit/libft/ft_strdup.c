#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*s2;
	int		strlen;

	i = 0;
	while (s1[i] != '\0')
	{
		i++;
		strlen = i;
	}
	i = 0;
	s2 = (char*)malloc((strlen + 1) * sizeof(*s1));
	if (!s2)
		return (NULL);
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
